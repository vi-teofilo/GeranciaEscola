package model;

public class Estado {
	private String nome;
	private String up;
	
public Estado() {
	
}
	
	public Estado(String nome, String up) {
		
		this.nome = nome;
		this.up = up;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getUp() {
		return up;
	}

	public void setUp(String up) {
		this.up = up;
	}
	
	
	
	
	
	
}
